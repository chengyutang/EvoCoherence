public class WeightSign {
	private double weight;
	private int sign;

	// Constructor
	public WeightSign(double weight, int sign) {
		this.weight = weight;
		this.sign = sign;
	}

	public double getWeight() {
		return this.weight;
	}

	public void setWeight(double weight) {
		this.weight = weight;
	}

	public int getSign() {
		return this.sign;
	}

	public void setSign(int sign) {
		this.sign = sign;
	}
}